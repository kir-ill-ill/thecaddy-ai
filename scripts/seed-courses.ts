async function main() {
  const databaseUrl = process.env.DATABASE_URL;
  if (!databaseUrl) {
    console.error(
      'DATABASE_URL is not set.\n' +
      'Provide it as an environment variable or use:\n' +
      'npx dotenv -e .env.local -- npx tsx scripts/seed-courses.ts'
    );
    process.exit(1);
  }

  const masked = databaseUrl.replace(/\/\/[^@]+@/, '//***:***@');
  console.log(`\nConnecting to: ${masked}\n`);

  const sql = neon(databaseUrl);

  // -----------------------------------------------------------------------
  // Step 1: Insert Destinations (check-before-insert for idempotency)
  // -----------------------------------------------------------------------
  console.log(`Seeding ${destinations.length} destinations...\n`);

  // Map destination name -> id for FK lookups
  const destIdMap: Record<string, number> = {};

  for (const dest of destinations) {
    try {
      // Check if already exists
      const existing = await sql`
        SELECT id FROM destinations WHERE name = ${dest.name} AND state = ${dest.state} LIMIT 1
      `;

      if (existing.length > 0) {
        destIdMap[dest.name] = existing[0].id;
        console.log(`  = ${dest.name}, ${dest.state} (already exists, id=${existing[0].id})`);
        continue;
      }

      const result = await sql`
        INSERT INTO destinations (
          name, region, state, description, vibe,
          avg_green_fee_low, avg_green_fee_high, avg_lodging_per_night,
          best_months, lat, lng
        ) VALUES (
          ${dest.name}, ${dest.region}, ${dest.state}, ${dest.description},
          ${dest.vibe}, ${dest.avg_green_fee_low}, ${dest.avg_green_fee_high},
          ${dest.avg_lodging_per_night}, ${dest.best_months}, ${dest.lat}, ${dest.lng}
        )
        RETURNING id
      `;

      destIdMap[dest.name] = result[0].id;
      console.log(`  + ${dest.name}, ${dest.state} (id=${result[0].id})`);
    } catch (err) {
      console.error(`  ! Failed to insert destination ${dest.name}:`, (err as Error).message);
    }
  }

  console.log(`\nDestinations done. ${Object.keys(destIdMap).length} mapped.\n`);

  // -----------------------------------------------------------------------
  // Step 2: Insert Courses (check-before-insert for idempotency)
  // -----------------------------------------------------------------------
  console.log(`Seeding ${courses.length} courses...\n`);

  let inserted = 0;
  let skipped = 0;
  let failed = 0;

  for (const course of courses) {
    const destinationId = destIdMap[course.destination_key];
    if (destinationId === undefined) {
      console.error(`  ! No destination found for key "${course.destination_key}" — skipping ${course.name}`);
      skipped++;
      continue;
    }

    try {
      // Check if already exists (by name + state)
      const existing = await sql`
        SELECT id FROM courses WHERE name = ${course.name} AND state = ${course.state} LIMIT 1
      `;

      if (existing.length > 0) {
        skipped++;
        continue;
      }

      await sql`
        INSERT INTO courses (
          name, destination_id, city, state, lat, lng,
          holes, par, slope_rating, course_rating, quality_score,
          green_fee_weekday, green_fee_weekend,
          cart_included, walking_allowed, resort_course, public_course,
          course_type, designer, year_built,
          description, website, phone, amenities
        ) VALUES (
          ${course.name}, ${destinationId}, ${course.city}, ${course.state},
          ${course.lat}, ${course.lng}, ${course.holes}, ${course.par},
          ${course.slope_rating}, ${course.course_rating}, ${course.quality_score},
          ${course.green_fee_weekday}, ${course.green_fee_weekend},
          ${course.cart_included}, ${course.walking_allowed},
          ${course.resort_course}, ${course.public_course},
          ${course.course_type}, ${course.designer}, ${course.year_built},
          ${course.description}, ${course.website}, ${course.phone},
          ${course.amenities}
        )
      `;

      inserted++;
    } catch (err) {
      console.error(`  ! Failed to insert course ${course.name}:`, (err as Error).message);
      failed++;
    }
  }

  // -----------------------------------------------------------------------
  // Summary
  // -----------------------------------------------------------------------
  console.log('\n' + '='.repeat(50));
  console.log('  Seed complete.');
  console.log(`  Destinations: ${Object.keys(destIdMap).length}`);
  console.log(`  Courses inserted: ${inserted}`);
  console.log(`  Courses skipped (already exist): ${skipped}`);
  if (failed > 0) {
    console.log(`  Courses failed: ${failed}`);
  }
  console.log(`  Total courses in seed: ${courses.length}`);
  console.log('='.repeat(50) + '\n');

  if (failed > 0) {
    process.exit(1);
  }
}

main().catch((err) => {
  console.error('Fatal error:', err);
  process.exit(1);
});
